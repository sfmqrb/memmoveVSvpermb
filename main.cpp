inline void shift_right_memmove(uint8_t* data, int k) {
    std::memmove(data + k + 1, data + k, 64 - k - 1);
}

// Shift right at byte offset k using AVX-512 VPERMB (in-place)
inline void shift_right_vpermb(uint8_t* data, int k) {
    __m512i v = _mm512_loadu_si512((const __m512i*)data);
    __m512i shuffled = _mm512_permutexvar_epi8(SHUFFLE[k], v);
    _mm512_storeu_si512((__m512i*)data, shuffled);
}

int main() {
    const int N = 1000;
    std::mt19937_64 rng{std::random_device{}()};
    std::uniform_int_distribution<int> byte_dist(0, 255);
    std::uniform_int_distribution<int> k_dist(1, 63);

    // Two distinct data sets, each mutated in-place
    std::vector<std::array<uint8_t,64>> data_mem(N), data_vp(N);
    std::vector<int> shifts_mem(N), shifts_vp(N);

    // Initialize randomly
    for (int i = 0; i < N; ++i) {
        for (int b = 0; b < 64; ++b) {
            data_mem[i][b] = static_cast<uint8_t>(byte_dist(rng));
            data_vp[i][b]  = static_cast<uint8_t>(byte_dist(rng));
        }
        shifts_mem[i] = k_dist(rng);
        shifts_vp[i]  = k_dist(rng);
    }

    // print all the elements of the first array
    for (int i = 0; i < 64; ++i) {
        std::printf("%d ", data_mem[0][i]);
    }
    std::printf("\n");

    // Benchmark memmove version (in-place)
    auto t0 = std::chrono::high_resolution_clock::now();
    for (int i = 0; i < N; ++i) {
        shift_right_memmove(data_mem[i].data(), shifts_mem[i]);
    }
    auto t1 = std::chrono::high_resolution_clock::now();
    auto dt_mem = std::chrono::duration_cast<std::chrono::nanoseconds>(t1 - t0).count();

    // print all elements of the first array 
    for (int i = 0; i < 64; ++i) {
        std::printf("%d ", data_mem[0][i]);
    }
    std::printf("\n");


    // Benchmark VPERMB version (in-place)
    auto t2 = std::chrono::high_resolution_clock::now();
    for (int i = 0; i < N; ++i) {
        shift_right_vpermb(data_vp[i].data(), shifts_vp[i]);
    }
    auto t3 = std::chrono::high_resolution_clock::now();
    auto dt_vp = std::chrono::duration_cast<std::chrono::nanoseconds>(t3 - t2).count();

    double avg_mem_ns = double(dt_mem) / N;
    double avg_vp_ns  = double(dt_vp ) / N;

    std::printf("Average shift_right_memmove: %.1f ns\n", avg_mem_ns);
    std::printf("Average shift_right_vpermb:  %.1f ns\n", avg_vp_ns);

    return 0;
}
